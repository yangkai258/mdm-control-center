for filename, cfg in modules.items():
    filepath = os.path.join(DOCS_DIR, filename)
    if not os.path.exists(filepath):
        print(f"SKIP (not found): {filename}")
        continue

    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()

    insert_before = cfg["insert_before"]
    if insert_before not in content:
        print(f"SKIP (marker not found): {filename} - looking for {insert_before}")
        continue

    ui_content = UI_SECTION_TEMPLATE.format(
        section_num=cfg["section_num"],
        menu_path=cfg["menu_path"],
        top_area=cfg["top_area"],
        middle_area=cfg["middle_area"],
        components=cfg["components"],
        reference_template=cfg["reference_template"],
        interaction_flow=cfg["interaction_flow"],
        key_states=cfg["key_states"]
    )

    # Insert the new section
    new_content = content.replace(insert_before, ui_content + "\n\n" + insert_before)

    # Renumber sections that come after (## next_num → ## next_num+1)
    # Only renumber the "## X、" style section headers that are >= section_num
    # We need to find and replace ## [next_section_num]、 → ## [next_section_num+1]、
    # and also ## [section_num]、 → ## [section_num+1]、

    # Simple approach: just find all "## 数字、" patterns and renumber from section_num onwards
    # This is tricky because we need to avoid renumbering things like version numbers
    # But since all our sections follow "## 八、" "## 九、" pattern, let's just do targeted replacement

    # Actually, let's just not renumber - the UI section just gets added before
    # and we update the section number of the inserted section
    # The existing sections keep their numbers - it's a minor inconsistency in numbering
    # But this is acceptable for a PRD document

    with open(filepath, "w", encoding="utf-8") as f:
        f.write(new_content)

    print(f"OK: {filename}")

