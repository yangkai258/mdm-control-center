for filename, cfg in modules.items():
    filepath = os.path.join(DOCS_DIR, filename)
    if not os.path.exists(filepath):
        print(f"SKIP (not found): {filename}")
        continue

    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()

    # Find the section marker
    marker = cfg["section_before"]
    if marker not in content:
        print(f"SKIP (marker not found): {filename} - looking for {marker}")
        continue

    ui_content = UI_SECTION.format(
        section_num=cfg["section_num"],
        menu_path=cfg["menu_path"],
        top_area=cfg["top_area"],
        middle_area=cfg["middle_area"],
        components=cfg["components"],
        参考模板内容=cfg["template"],
        交互流程内容=cfg["flow"],
        key_states=cfg["key_states"]
    )

    new_content = content.replace(marker, ui_content + "\n\n" + marker)
    with open(filepath, "w", encoding="utf-8") as f:
        f.write(new_content)

    print(f"OK: {filename}")

